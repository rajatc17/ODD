        String regex = "^[A-Za-z0-9+_.-]+@(.+)$";
 
    Pattern pattern = Pattern.compile(regex);
    Matcher matcher = pattern.matcher(we.getText());
        if(wn.getText().equals("") || wu.getText().equals("") || wp.getPassword().equals("") || wph.getText().equals("") || we.getText().equals(""))
    {
        JOptionPane.showMessageDialog(this,"ENTER ALL FIELDS");
        wn.setText("");wu.setText("");wp.setText("");wph.setText("");we.setText("");
    }
    else if(wph.getText().length()!=10)
    {
        JOptionPane.showMessageDialog(this,"PHONE NUMBER SHOULD BE 10 DIGITS EXACT!");
        wn.setText("");wu.setText("");wp.setText("");wph.setText("");we.setText("");
        
    }
 
    
    else if(!matcher.matches())
    {
        JOptionPane.showMessageDialog(this,"NOT A VALID EMAIL ADDRESS!");
    }

    else {
        add_to_order();
    }
   
                                      
