          Map<String,Integer> taken = new HashMap<String,Integer>();
          
          private void initList()
    {
        for(int i=1;i<=100;i++)
        {
            wid.add("W"+i);
        }
        this.wid=wid;
    }
          
           private void add_to_map()
    {
        
        try
        { 
            Connection cn=Connectivity.getConnection();
            PreparedStatement ps=cn.prepareStatement("select WPID from warepro");
            ResultSet rs=ps.executeQuery();
            while(rs.next())
            {
                taken.put(rs.getString("WPID"),1);
            }
        }
    catch(Exception e)
    {   
        JOptionPane.showMessageDialog(this,e);
    }
    }
           
    private void add_to_order()
    {
         add_to_map();
         String wp_id=null;
         for(String str:wid)
         {
             if(!taken.containsKey(str))
             {
                 wp_id=str;
                 break;
             }
         }
         
         try
         {
            Connection con=Connectivity.getConnection();
            String sql ="insert into warepro(WPID,Name,User,Pass,Contact,Mail,Verify) values (?,?,?,?,?,?,'NO')";
            PreparedStatement ps=con.prepareStatement(sql);
            ps.setString(1,wp_id);
            ps.setString(2,wn.getText());
            ps.setString(3, wu.getText());
            ps.setString(4, new String(wp.getPassword()));
            ps.setString(5,wph.getText());
            ps.setString(6,we.getText());
            ps.execute();
            JOptionPane.showMessageDialog(this,"Saved Successfully");
            wn.setText("");
            wu.setText("");
            wp.setText("");
            wph.setText("");
            we.setText("");
                
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e);
        }
    }

